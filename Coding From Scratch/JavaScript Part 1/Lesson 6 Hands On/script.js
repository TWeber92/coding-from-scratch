var inputGrade = prompt("Enter a Grade");
var numberGrade = parseInt(inputGrade);
var letterGrade

if (numberGrade >= 90) {
    alert(letterGrade ="A");
}
else if (numberGrade >= 80) {
    alert(letterGrade ="B");
}
else if (numberGrade >= 70) {
    alert(letterGrade ="C");
}
else if (numberGrade >= 60) {
    alert(letterGrade ="D");
}
else {
    alert("F");
}